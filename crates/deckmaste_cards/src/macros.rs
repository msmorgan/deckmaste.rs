    /// Parses a definition the way plugin loading does: from RON source in
    /// deckmaste's dialect. (`MacroDef`'s body field is crate-private in
    /// `macro_ron`, so file-shaped source is the construction path here —
    /// which is also what real definitions are.)
    fn def(source: &str) -> MacroDef {
        deckmaste_core::ron::options().from_str(source).unwrap()
    }

    /// The registry matches on serde type names. The derived `kind()`s
    /// self-name from the Rust ident, so they track renames by
    /// construction; the tie this pins is the hand-registered struct kinds
    /// (`CardFace`, `Subtype`, `Macro`), which a Rust rename would strand
    /// without a compile error — plus the policy list's completeness (the
    /// length check).
    #[test]
    fn kind_names_track_the_core_types() {
        fn name_of<T>() -> &'static str {
            std::any::type_name::<T>().rsplit("::").next().unwrap()
        }
        let names = [
            name_of::<Ability>(),
            name_of::<Action>(),
            name_of::<AsThough>(),
            name_of::<CardFace>(),
            name_of::<Condition>(),
            name_of::<Count>(),
            name_of::<CostComponent>(),
            name_of::<Effect>(),
            name_of::<Event>(),
            name_of::<Filter>(),
            name_of::<KeywordAbility>(),
            name_of::<ManaRider>(),
            name_of::<PlayerAction>(),
            name_of::<Preference>(),
            name_of::<Quantity>(),
            name_of::<Reference>(),
            name_of::<Replacement>(),
            name_of::<Selection>(),
            name_of::<StaticEffect>(),
            name_of::<Subtype>(),
            name_of::<TargetSpec>(),
            name_of::<Counter>(),
            "Macro", // hand-registered: MacroDef's serde rename, loader-only
        ];
        let kinds = kinds();
        for name in names {
            assert!(kinds.contains(name), "`{name}` is not a registered kind");
        }
        assert_eq!(kinds.len(), names.len());
    }

    /// Filter's generated Deserialize calls `deserialize_enum` with the
    /// full flattened variant list (its own names plus the compartments'):
    /// that is what lets unknown names at Filter positions fall through to
    /// the macro namespace.
    #[test]
    fn filter_positions_expand_macros() {
        let mut macros = macro_set();
        macros
            .insert(&def(r#"(
                    name: "AnyTargetish",
                    kinds: [Filter],
                    body: OneOf([Kind(Player), AllOf([InZone(Battlefield), Type(Creature)])]),
                )"#))
            .unwrap();
        let filter: Filter = macros.read_str("AnyTargetish").unwrap();
        // The invocation is remembered; the expansion lives under `.value`.
        let Filter::Expanded(expanded) = filter else {
            panic!("expected a remembered filter, got {filter:?}");
        };
        assert_eq!(expanded.name, "AnyTargetish");
        let Filter::OneOf(arms) = *expanded.value else {
            panic!("expected OneOf, got {:?}", expanded.value);
        };
        assert_eq!(arms[0], Filter::Kind(ObjectKind::Player));
        // The nested arm proves Filter positions *inside* an expansion stay
        // macro-aware too.
        assert_eq!(
            arms[1],
            Filter::AllOf(vec![
                Filter::State(StateFilter::InZone(Zone::Battlefield)),
                Filter::Characteristic(CharacteristicFilter::Type(Type::Creature)),
            ])
        );
        assert_eq!(arms.len(), 2);
    }

