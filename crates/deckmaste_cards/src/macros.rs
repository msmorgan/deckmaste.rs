//! The card-domain macro configuration: which core types are macroable, and
//! with what reader policy, glued onto [`macro_ron`]. Definitions live in
//! `plugins/*/macros/**/*.ron` (paths and file names are organizational
//! only) and are invoked by name where a value of one of their kinds is
//! expected — see [`crate::plugin`] for loading and the `macro_ron` crate
//! docs for the language itself.
//!
//! Subtype definitions are ordinary macro files, usually written as
//! meta-macro invocations (`CreatureType(name: "TimeLord", template:
//! "Time Lord")`) — `name` is the registration ident cards invoke,
//! `template` the printed name carried in the value.

pub use macro_ron::InsertError;
use macro_ron::KindSet;
pub use macro_ron::MacroDef;
pub use macro_ron::MacroSet;
pub use macro_ron::ParamType;
pub use macro_ron::ParamTypeSet;
pub use macro_ron::Params;

/// The kinds of value a macro can expand to: the core types whose parse
/// positions consult the macro namespace. Which types are macroable is
/// consumer policy (this list); the per-kind facts (remembers / embeds /
/// literal wrapper) are generated by each type's `#[derive(SupportsMacros)]`.
/// So `Count::kind()` carries the bare-numeral `Literal(N)` reader sugar
/// (core's grammar stays strict); `Selection::kind()` remembers *and*
/// embeds its `Reference` untagged, so a bare `This`/`Target(0)` at a
/// Selection slot reads as `Selection::Ref(...)` with no wrapper; and
/// `Action::kind()` — no `Expanded` variant, so no macros of its own —
/// only embeds `PlayerAction` untagged, so a bare `Draw(1)`/`Tap(This)`
/// reads as `Action::By(You, ...)`, the implicit-you default.
///
/// The struct kinds `CardFace`, `Subtype`, and `Macro` are name-erasing:
/// `Subtype` already self-names; nothing engine-meaningful invokes `CardFace`
/// macros; and `Macro` is the loader-only position for `MacroDef` itself.
// When adding a new macroable core type, add its `T::kind()` to the shared
// registry in `deckmaste_core::ron::kinds` AND its name in
// `kind_names_track_the_core_types`.
#[must_use]
pub fn kinds() -> KindSet {
    use deckmaste_core::SupportsMacros as _;
    // Core types come from the shared registry; cards-only macroable types
    // (the strategy `Preference`) are added on top.
    let mut kinds = deckmaste_core::ron::kinds();
    kinds.add(crate::strategy::Preference::kind());
    kinds
}

/// The param types in scope for deckmaste plugins: the domain-neutral
/// built-ins (`Any`, `String`) plus deckmaste's `Color`, `Filter`, and
/// `Reference`. A validator reads the argument as its Rust type with macros
/// in scope, so the check and the real grammar are one path.
#[must_use]
pub fn param_types() -> ParamTypeSet {
    let mut param_types = ParamTypeSet::default();
    param_types.add_typed::<deckmaste_core::Color>("Color");
    param_types.add_typed::<deckmaste_core::Filter>("Filter");
    // A reference slot: the object/player a macro compares against
    // (`SharesColorWith(Ref(This))`, `SharesColor(Subject, This)`). Also types
    // regeneration's `Regenerate(target)` param.
    param_types.add_typed::<deckmaste_core::Reference>("Reference");
    // A keyword/cost slot: the bracketed cost-component list authored as the
    // macro's argument (`[Mana([Generic(2)])]`, `[Mana(...), Do(LoseLife(2))]`).
    param_types.add_typed::<Vec<deckmaste_core::CostComponent>>("Cost");
    // A count slot (`PumpThisUntilEot`'s P/T magnitudes): a bare numeral reads as
    // `Count::Literal`, dynamic counts as `CountOf`/`StatOf`.
    param_types.add_typed::<deckmaste_core::Count>("Count");
    param_types
}

/// An empty [`MacroSet`] over the card kinds, reading deckmaste's RON
/// dialect.
#[must_use]
pub fn macro_set() -> MacroSet {
    MacroSet::new(kinds())
        .with_options(deckmaste_core::ron::raw_options())
        .with_param_types(param_types())
}

