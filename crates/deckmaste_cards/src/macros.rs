    /// A remembered invocation round-trips as the invocation through the
    /// real core types' `Serialize` impls: a nullary Ability
    /// macro serializes back to its bare name, a parameterized Filter macro
    /// to the original call text — not the expansion.
    #[test]
    fn remembered_invocations_round_trip_as_invocations() {
        let mut macros = macro_set();
        macros
            .insert(&def(r#"(
                    name: "Flying",
                    kinds: [Ability],
                    body: Static(effects: [Cant(Attack(by: Ref(This)))]),
                )"#))
            .unwrap();
        macros
            .insert(&def(r#"(
                    name: "OfType",
                    kinds: [Filter],
                    params: [Any],
                    body: Type(Param(0)),
                )"#))
            .unwrap();

        let ability: Ability = macros.read_str("Flying").unwrap();
        assert_eq!(
            deckmaste_core::ron::options().to_string(&ability).unwrap(),
            "Flying"
        );

        let filter: Filter = macros.read_str("OfType(Creature)").unwrap();
        assert_eq!(
            deckmaste_core::ron::options().to_string(&filter).unwrap(),
            "OfType(Creature)"
        );
    }

    /// `expand_all` over a real converted type: an `Effect` read through the
    /// card macro set carries the remembered `Expanded` node (here nested in
    /// a `Sequence`, proving recursion through real grammar containers), and
    /// expanding strips it — the result serializes as the fully-expanded RON,
    /// not the invocation.
    #[test]
    fn expand_all_strips_remembered_invocations() {
        use deckmaste_core::Expand as _;

        let mut macros = macro_set();
        macros
            .insert(&def(r#"(
                    name: "DrawTwo",
                    kinds: [Effect],
                    body: Draw(2),
                )"#))
            .unwrap();
        let effect: Effect = macros.read_str("Sequence([DrawTwo])").unwrap();
        let Effect::Sequence(steps) = &effect else {
            panic!("expected a sequence, got {effect:?}");
        };
        assert!(matches!(steps[0], Effect::Expanded(_)));

        let expanded = effect.expand_all();
        assert_eq!(
            expanded,
            Effect::Sequence(vec![Effect::Act(Action::By(
                Reference::You,
                PlayerAction::Draw(Count::Literal(2)),
            ))])
        );
        let written = deckmaste_core::ron::options().to_string(&expanded).unwrap();
        assert!(!written.contains("DrawTwo"), "macro name leaked: {written}");
        // A `Count` literal writes bare — `2`, never `Literal(2)`.
        assert_eq!(written, "Sequence([Draw(2)])");
    }

